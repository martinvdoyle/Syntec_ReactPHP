CREATE TABLE IF NOT EXISTS syntec_messages (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  message_code VARCHAR(20) NULL,
  name VARCHAR(100) NULL,
  organisation VARCHAR(100) NULL,
  email VARCHAR(100) NULL,
  phone VARCHAR(30) NULL,
  message LONGTEXT NULL,
  message_date DATE NULL,
  message_time VARCHAR(8) NULL,
  message_type_code VARCHAR(20) NULL,
  source_type_code VARCHAR(20) NULL,
  created_date DATE NULL,
  product_id VARCHAR(200) NULL,
  subject VARCHAR(255) NULL,
  enquiry_type_code VARCHAR(20) NULL,
  ip_address VARCHAR(45) NULL,
  city VARCHAR(100) NULL,
  region VARCHAR(100) NULL,
  country_name VARCHAR(100) NULL,
  device_type VARCHAR(50) NULL,
  browser_type VARCHAR(50) NULL,
  user_agent VARCHAR(4000) NULL,
  os VARCHAR(50) NULL,
  website_user_email VARCHAR(255) NULL,
  message_ref VARCHAR(30) NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY uq_syntec_messages_code (message_code),
  KEY idx_syntec_messages_message_type_code (message_type_code),
  KEY idx_syntec_messages_source_type_code (source_type_code),
  KEY idx_syntec_messages_enquiry_type_code (enquiry_type_code),
  KEY idx_syntec_messages_product_id (product_id),
  KEY idx_syntec_messages_created_date (created_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
