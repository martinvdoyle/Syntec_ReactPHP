-- Oracle SUPPLIERS direct migration to syntec_suppliers
-- Uses a full Oracle-shaped staging table, then upserts runtime fields.

CREATE TABLE IF NOT EXISTS syntec_suppliers_staging (
  deleted CHAR(1) NULL,
  active CHAR(1) NULL,
  supplier_id VARCHAR(20) NOT NULL,
  supplier_name VARCHAR(255) NOT NULL,
  short_name VARCHAR(255) NULL,
  class_id VARCHAR(100) NULL,
  class_colour VARCHAR(20) NULL,
  profile_1 LONGTEXT NULL,
  profile_2 LONGTEXT NULL,
  business VARCHAR(255) NULL,
  website VARCHAR(1024) NULL,
  supplier_logo_large VARCHAR(255) NULL,
  supplier_logo_small VARCHAR(255) NULL,
  supplier_image_1 VARCHAR(255) NULL,
  supplier_image_2 VARCHAR(255) NULL,
  supplier_image_3 VARCHAR(255) NULL,
  supplier_image_4 VARCHAR(255) NULL,
  address_1 VARCHAR(255) NULL,
  address_2 VARCHAR(255) NULL,
  address_3 VARCHAR(255) NULL,
  address_4 VARCHAR(255) NULL,
  address_5 VARCHAR(255) NULL,
  date_created DATETIME NULL,
  date_deleted DATETIME NULL,
  anchor_id VARCHAR(255) NULL,
  supplier_image_background VARCHAR(255) NULL,
  supplier_logo_large_scale_smaller VARCHAR(100) NULL,
  PRIMARY KEY (supplier_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

INSERT INTO syntec_suppliers (
  supplier_id,
