  active_flag = VALUES(active_flag),
  updated_at = NOW();

SELECT COUNT(*) AS staged_rows FROM syntec_suppliers_staging;
SELECT COUNT(*) AS runtime_rows FROM syntec_suppliers;
SELECT COUNT(*) AS missing_supplier_id
FROM syntec_suppliers
WHERE supplier_id IS NULL OR supplier_id = '';
