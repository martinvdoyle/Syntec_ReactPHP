SELECT
  s.supplier_id,
  s.supplier_name,
  s.short_name,
  s.class_id,
  s.class_colour,
  s.profile_1,
  s.profile_2,
  s.business,
  s.website,
  s.supplier_logo_large,
  s.supplier_logo_small,
  s.supplier_image_1,
  s.supplier_image_2,
  s.supplier_image_3,
  s.supplier_image_4,
  s.address_1,
  s.address_2,
  s.address_3,
  s.address_4,
  s.address_5,
  s.date_created,
  s.date_deleted,
  s.anchor_id,
  s.supplier_image_background,
  s.supplier_logo_large_scale_smaller,
  COALESCE(NULLIF(TRIM(s.anchor_id), ''), LOWER(REPLACE(REPLACE(REPLACE(TRIM(s.supplier_name), ' ', '-'), '/', '-'), '&', 'and'))) AS slug,
  0,
  CASE WHEN COALESCE(s.active, 'Y') = 'Y' THEN 1 ELSE 0 END,
  COALESCE(s.date_created, NOW()),
  NOW()
FROM syntec_suppliers_staging s
ON DUPLICATE KEY UPDATE
  supplier_name = VALUES(supplier_name),
  short_name = VALUES(short_name),
  class_id = VALUES(class_id),
  class_colour = VALUES(class_colour),
  profile_1 = VALUES(profile_1),
  profile_2 = VALUES(profile_2),
